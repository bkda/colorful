#[derive(Copy, Clone)]
pub enum ColorMode {
    SIMPLE,
    RGB,
    HSL,
}

#[derive(Clone)]
pub struct Colorado {
    pub mode: ColorMode,
    pub color: String,
}


impl Default for Colorado {
    fn default() -> Colorado {
        Colorado {
            mode: ColorMode::SIMPLE,
            color: String::default(),
        }
    }
}

impl Colorado {
    pub fn new<C: ColorInterface>(color: C) -> Colorado {
        let c = format!("{}", color.to_color_str());
        Colorado {
            color: format!("{}", color.to_color_str()),
            mode: if c.contains(";") {
                ColorMode::RGB
            } else {
                ColorMode::SIMPLE
            },
        }
    }
}


#[test]
fn test_color() {
    let s = "Hello world";
    let colors = all_colors();
    for (i, color) in colors.iter().enumerate() {
        assert_eq!(format!("\x1B[38;5;{}mHello world\x1B[0m", i.to_owned()), s.color(*color).to_string());
    }
}


